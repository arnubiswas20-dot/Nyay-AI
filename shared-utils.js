// ============================================

function generateEvidenceId(type = 'CRIME') {
    const year = new Date().getFullYear();
    const random = Math.floor(Math.random() * 999999).toString().padStart(6, '0');
    
    if (type === 'MEDICAL') {
        return `MLC-${year}-${random}`;
    }
    return `EV-${year}-${random}`;
}

