function saveEvidence(evidence) {
    // Get existing evidence lists
    let evidenceList = JSON.parse(localStorage.getItem('nyai_evidence_list') || '[]');
    
    // Add to appropriate list based on type
    if (evidence.type === 'MEDICAL_EVIDENCE') {
        let medicalList = JSON.parse(localStorage.getItem('nyai_medical_evidence_list') || '[]');
        medicalList.push(evidence.id);
        localStorage.setItem('nyai_medical_evidence_list', JSON.stringify(medicalList));
    } else {
        let crimeList = JSON.parse(localStorage.getItem('nyai_crime_evidence_list') || '[]');
        crimeList.push(evidence.id);
        localStorage.setItem('nyai_crime_evidence_list', JSON.stringify(crimeList));
    }
    
    evidenceList.push(evidence.id);
    localStorage.setItem('nyai_evidence_list', JSON.stringify(evidenceList));

    // Save individual evidence
    localStorage.setItem(`nyai_evidence_${evidence.id}`, JSON.stringify(evidence));

    // Initialize empty custody chain
    localStorage.setItem(`nyai_custody_${evidence.id}`, JSON.stringify([]));
    
    // Log the action
    logAction('EVIDENCE_REGISTRATION', {
        evidenceId: evidence.id,
        type: evidence.type,
        caseNumber: evidence.caseNumber,
        category: evidence.category
    });
}

function getEvidence(evidenceId) {
    const evidenceData = localStorage.getItem(`nyai_evidence_${evidenceId}`);
    return evidenceData ? JSON.parse(evidenceData) : null;
}

function updateEvidence(evidenceId, updates) {
    const evidence = getEvidence(evidenceId);
    if (!evidence) return null;
    
    const updatedEvidence = { ...evidence, ...updates };
    localStorage.setItem(`nyai_evidence_${evidenceId}`, JSON.stringify(updatedEvidence));
    
    return updatedEvidence;
}

