// PERMISSION CHECKING
// ============================================

function checkPermission(action) {
    const session = getSession();
    if (!session || !session.permissions) return false;
    
    const permissionMap = {
        'register_evidence': 'canRegisterEvidence',
        'register_crime': 'canRegisterCrimeScene',
        'register_medical': 'canRegisterMedical',
        'transfer_custody': 'canTransferCustody',
        'verify_integrity': 'canVerifyIntegrity',
        'generate_report': 'canGenerateReport',
        'add_lab_findings': 'canAddLabFindings'
    };
    
    const permissionKey = permissionMap[action];
    return permissionKey ? session.permissions[permissionKey] === true : false;
}

function blockUnauthorizedAction(actionName) {
    const session = getSession();
    const roleName = session ? session.role.replace(/_/g, ' ') : 'Unknown';
    
    alert(`🚫 ACCESS DENIED\n\nRole: ${roleName}\nAction: ${actionName}\n\nYou do not have permission to perform this action.`);
    
    // Log unauthorized attempt
    logAction('UNAUTHORIZED_ATTEMPT', {
        attemptedAction: actionName,
        denied: true
    });
}

