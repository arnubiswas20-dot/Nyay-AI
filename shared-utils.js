function addLabFinding(evidenceId, finding) {
    const findings = JSON.parse(localStorage.getItem(`nyai_lab_findings_${evidenceId}`) || '[]');
    
    const newFinding = {
        id: `FIND-${Date.now()}`,
        timestamp: new Date().toISOString(),
        ...finding
    };
    
    findings.push(newFinding);
    localStorage.setItem(`nyai_lab_findings_${evidenceId}`, JSON.stringify(findings));
    
    // Log the action
    logAction('LAB_FINDING_ADDED', {
        evidenceId: evidenceId,
        findingId: newFinding.id,
        findingType: finding.type
    });
    
    return newFinding;
}

function getLabFindings(evidenceId) {
    return JSON.parse(localStorage.getItem(`nyai_lab_findings_${evidenceId}`) || '[]');
}

