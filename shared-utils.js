// FORENSIC AUDIT LOGGING
// ============================================

function createForensicLog(roleId, action, details) {
    // Format: 2026-05-27_[RoleID]_[Action].md
    const date = new Date().toISOString().split('T')[0];
    const timestamp = new Date().toLocaleString('en-IN', { timeZone: 'Asia/Kolkata' });
    
    const logEntry = {
        filename: `${date}_${roleId}_${action}.md`,
        timestamp: new Date().toISOString(),
        content: `# Forensic Custody Log Entry

**Date:** ${timestamp}
**Role ID:** ${roleId}
**Action:** ${action}
**Session ID:** ${details.sessionId || 'N/A'}

## Details
\`\`\`json
${JSON.stringify(details, null, 2)}
\`\`\`

## Audit Trail
This action has been logged in the immutable forensic custody chain.
All timestamps are in IST (Indian Standard Time).

---
*Generated by Nyay AI - Digital Forensic Chain Ledger*
`
    };

    // Store log in localStorage (in production, this would be sent to backend)
    const logs = JSON.parse(localStorage.getItem('nyai_forensic_logs') || '[]');
    logs.push(logEntry);
    localStorage.setItem('nyai_forensic_logs', JSON.stringify(logs));
    
    console.log(`📝 Forensic log created: ${logEntry.filename}`);
    return logEntry;
}

function logAction(action, details) {
    const session = getSession();
    if (!session) return;
    
    createForensicLog(session.credentialId, action, {
        ...details,
        role: session.role,
        userName: session.name,
        sessionId: session.sessionId,
        timestamp: new Date().toISOString()
    });
}

// ============================================
// CRYPTOGRAPHIC HASH FUNCTIONS
// ============================================

async function generateSHA256(data) {
    const encoder = new TextEncoder();
    const dataBuffer = encoder.encode(data);
    const hashBuffer = await crypto.subtle.digest('SHA-256', dataBuffer);
    const hashArray = Array.from(new Uint8Array(hashBuffer));
    return hashArray.map(b => b.toString(16).padStart(2, '0')).join('');
}

async function verifyPhotoHash(originalHash, photoBase64) {
    const newHash = await generateSHA256(photoBase64);
    return {
        match: originalHash === newHash,
        originalHash: originalHash,
        newHash: newHash
    };
}

