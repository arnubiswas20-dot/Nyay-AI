function createTamperAlert(evidenceId, details) {
    const alerts = JSON.parse(localStorage.getItem('nyai_tamper_alerts') || '[]');
    
    const alert = {
        id: `ALERT-${Date.now()}`,
        evidenceId: evidenceId,
        timestamp: new Date().toISOString(),
        severity: 'CRITICAL',
        ...details
    };
    
    alerts.push(alert);
    localStorage.setItem('nyai_tamper_alerts', JSON.stringify(alerts));
    
    // Log the critical event
    logAction('TAMPER_DETECTED', {
        evidenceId: evidenceId,
        alertId: alert.id,
        details: details
    });
    
    return alert;
}

function getTamperAlerts() {
    return JSON.parse(localStorage.getItem('nyai_tamper_alerts') || '[]');
}

